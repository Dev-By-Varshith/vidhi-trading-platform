// ─── Constants ────────────────────────────────────────────────────────────────
const TICK_SIZE      = 0.01;
const MAX_LEVELS     = 200;
let   BASE_PRICE     = 1500.00;  // overridden per-round via payload.basePrice
const TICK_BATCH     = 500;
let MAX_TICKS        = 100_000;
const POSITION_LIMIT = 1000;
